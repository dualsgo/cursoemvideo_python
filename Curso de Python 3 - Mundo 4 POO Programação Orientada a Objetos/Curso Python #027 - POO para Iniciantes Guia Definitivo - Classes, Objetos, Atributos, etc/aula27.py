# O exemplo dos biscoitos:

# Os biscoitos tem o mesmo formato por utilizarem a mesma forma. Essa forma em POO é uma classe.

# A classe é o molde que define as caracteristicas do objeto.

# A classe possui um diagrama de blocos, onde cada bloco representa um atributo ou um método da classe.

# Nome da classe
# Características (atributos)
# Comportamentos (métodos)

# Exemplo da classe Biscoito:

# BiscoitoCoracao
# Características: tamanho, massa, peso, cobertura, cozido, temperatura
# Comportamentos: cozinhar(), congelar(), cobrir(), confeitar(), podeComer(), comer()

# Há uma sinalização com -, + e # para indicar se o atributo ou método é privado, público ou protegido. Porém em Python isso é apenas uma convenção, não há realmente um controle de acesso.

# Quando criamos um objeto a partir de uma classe, dizemos que estamos instanciando a classe. O objeto é uma instância da classe.

# Um objeto é uma instancia de uma classe. Ele possui as características e comportamentos definidos na classe, mas cada objeto pode ter valores diferentes para os atributos.

# Objeto é uma coisa material ou abstrata que é feita por meio de um modelo e pode ser descrita por meio das suas caracteristicas, comportamentos e estado atual.

# Estado: Conjunto de valores dos atributos de um objeto em um determinado momento. O estado pode mudar ao longo do tempo, dependendo das ações realizadas sobre o objeto.

# Biscoito
# tamanho = 8.2
# massa = baunilha
# peso = 54.3g
# cobertura = pistache
# cozido = True
# temperatura = 55c

# Exemplos de objetos abstratos:

# Uma consulta marcada no médico é um objeto abstrato, pois não é algo material, mas tem características (data, hora, local, médico, paciente) e comportamentos (marcar(), desmarcar(), reagendar()).

# Um processo de venda em uma loja é um objeto abstrato, pois não é algo material, mas tem características (produto, preço, quantidade, cliente) e comportamentos (adicionarProduto(), removerProduto(), finalizarVenda()).

# Entre outros, um compromisso, uma reunião, uma aula, uma transação bancária, uma reserva de voo, um erro de sistema. Todos possuem características e comportamentos, mas não são coisas materiais.

