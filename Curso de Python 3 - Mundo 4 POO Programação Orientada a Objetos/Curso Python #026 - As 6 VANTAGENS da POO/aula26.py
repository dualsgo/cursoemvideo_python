# Caracteristica 1: Confiança
# O isolamento entre as partes gera algo mais seguro. Ao alterar uma das partes, nenhuma outra é afetada.

# Caracteristica 2: Oportuno
# Ao dividir tudo em partes, cada uma delas pode ser desenvolvida em paralelo.

# Caracteristica 3: Manutenível
# Atualizar é mais fácil. Uma pequena alteração vai beneficiar todas as partes relacionadas.

# Caracteristica 4: Extensível
# Um sistema não deve ser estático. Tudo deve mudar e crescer para permanecer útil.

# Caracteristica 5: Reutilizável
# Objetos que foram criados em um sistema podem ser aproveitados em outros sistemas.

# Caracteristica 6: Natural
# Mais fácil de entender. Maior atenção as funcionalidades do que aos detalhes de implementação.

# Desafios

# Criei um sistema para Escola. Ao criar outro sistema para academia, parte do trabalho já estará feito, pois tenho Alunos nos dois. Qual característica da POO isso representa?
# Se precisar adicionar uma nova funcionalidade ao sistema, consigo fazer isso com grande facilidade.
# Posso desenvolver várias funcções que dependam uma da outra ao mesmo tempo se o planejamento for seguido.
# O código fica muito simples de ler e de compreender, já que não preciso saber os detalhes do funcionamento ao programar.
# Posso alterar como uma funcionalidade trabalha e tudo continua funcionando como antes.

# Respostas: 1 - Reutilizável, 2 - Extensível, 3 - Oportuno, 4 - Natural, 5 - Confiável

# O exemplo do carro:

# O carro é um sistema, composto por várias partes (objetos). O motor é um objeto, as rodas são objetos, o volante é um objeto, os bancos são objetos. Todos eles trabalham juntos para que o carro funcione.

# Se o pneu do carro furar, eu posso trocar o pneu sem precisar mexer no motor, no volante ou nos bancos. Isso é confiança.

# O carro é montado em uma linha de produção, onde cada parte é feita por um grupo diferente. O motor é feito por um grupo, as rodas por outro grupo, o volante por outro grupo, os bancos por outro grupo. Todos eles trabalham em paralelo para montar o carro. Isso é oportuno.

# Ao otimizar partes do carro, como o motor, eu posso melhorar o desempenho do carro sem precisar mexer nas rodas, no volante ou nos bancos. Isso é manutenível.

# Se eu quiser adicionar um sistema de som ao carro, eu posso fazer isso sem precisar mexer no motor, nas rodas, no volante ou nos bancos. Isso é extensível.

# Se um fabricante quiser utilizar o mesmo motor em vários modelos de carros, ele pode fazer isso sem precisar criar um motor novo para cada modelo. Isso é reutilizável.

# O carro é algo que as pessoas estão acostumadas a usar, então é fácil de entender como ele funciona mas não necessariamente como cada parte dele funciona. Isso é natural.